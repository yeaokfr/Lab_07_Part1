public class Main {
    public static void main(String[] args) {
        for (int i = 0; i <= 30; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int j = 30; j >= 0; j--) {
            System.out.print(j + " ");
        }
        System.out.println();

        for (int k = 0; k <= 18; k += 3) {
            System.out.print(k + " ");

        }
        System.out.println();

        for(int l = 10; l >= 0; l-= 2) {
            System.out.print(l + " ");
        }
    }
}